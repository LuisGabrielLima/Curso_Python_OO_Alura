# LISTA
lista = []
# ADICIONA ITEM À LISTA - STRING
lista.append("bola")
print(lista)
# REMOVE ULTIMO ITEM DA LISTA
lista.pop()
print(lista)

# ADICIONA ITEM À LISTA - INT
lista.append(1)
print(lista)
# REMOVE ULTIMO ITEM DA LISTA
lista.pop()
print(lista)
 
 # ADICIONA MAIS DE 1 INTEM À LISTA
lista.extend([1,2,3,4])
# FILTRA ITEM POR INDEX
print(lista[0])


lista2 = [1,2,3,4]
# MENOR ITEM DA LISTA
print(min(lista2))
# MAX ITEM DA LISTA
print(max(lista))


lista2 = ["a", "b", "c", "d"]
# MENOR ITEM DA LISTA
print(min(lista2))
# MAX ITEM DA LISTA
